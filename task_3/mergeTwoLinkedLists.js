// `l1, l2` это `ListNode` и  имеет  только `.x` и `.next`. Декларация обьекта ниже. Вы не можете вносить изменения в объект.
function ListNode(x) {
  this.value = x;
  this.next = null;
}


function mergeTwoLinkedLists(l1, l2) {
    if(!l1) return l2;
    if(!l2) return l1;
    l1.next = mergeTwoLinkedLists(l1.next, l2)
    return l1
}
